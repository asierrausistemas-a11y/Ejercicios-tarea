# Paso 1: Pedir la edad al usuario
edad = int(input("Ingrese su edad: "))

# Paso 2: Evaluar si la edad es mayor o igual a 18
if edad >= 18:
    # Paso 3: Mostrar mensaje si cumple la condición
    print("Puedes conducir")
else:
    # Paso 4: Mostrar mensaje si NO cumple la condición
    print("Aún no tienes edad para conducir")

# EJERCICIO 5: POSITIVO, NEGATIVO O CERO

# Paso 1: Pedir un número al usuario
numero = float(input("Ingrese un número: "))

# Paso 2: Evaluar si es positivo
if numero > 0:
    # Paso 3: Mostrar si es positivo
    print("El número es POSITIVO")

# Paso 4: Evaluar si es negativo
elif numero < 0:
    # Paso 5: Mostrar si es negativo
    print("El número es NEGATIVO")

# Paso 6: Si no es positivo ni negativo, es cero
else:
    # Paso 7: Mostrar que es cero
    print("El número es CERO")